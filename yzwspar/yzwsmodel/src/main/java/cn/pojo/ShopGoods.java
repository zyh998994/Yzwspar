package cn.pojo;

public class ShopGoods {
    private Integer sgid;
    private Integer shortid;
    private String goodsname;
    private Float goodspice;
    private Integer goodsales;
    private String goodsimg;

    public Integer getSgid() {
        return sgid;
    }

    public void setSgid(Integer sgid) {
        this.sgid = sgid;
    }

    public Integer getShortid() {
        return shortid;
    }

    public void setShortid(Integer shortid) {
        this.shortid = shortid;
    }

    public String getGoodsname() {
        return goodsname;
    }

    public void setGoodsname(String goodsname) {
        this.goodsname = goodsname;
    }

    public Float getGoodspice() {
        return goodspice;
    }

    public void setGoodspice(Float goodspice) {
        this.goodspice = goodspice;
    }

    public Integer getGoodsales() {
        return goodsales;
    }

    public void setGoodsales(Integer goodsales) {
        this.goodsales = goodsales;
    }

    public String getGoodsimg() {
        return goodsimg;
    }

    public void setGoodsimg(String goodsimg) {
        this.goodsimg = goodsimg;
    }
}
